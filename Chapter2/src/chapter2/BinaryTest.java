package chapter2;

public class BinaryTest {

	public static void main(String[] args) {

		int num = 10;
		int bNum = 0B1010; //10 2진법
		int oNum = 012; //8진법
		int hNum = 0XA; //16진
	
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}

}
